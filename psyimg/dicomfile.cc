  for(DICOM_Tag **tagListPtr = tagListList; *tagListPtr != NULL;  tagListPtr++) {
    DICOM_Tag *tagList = *tagListPtr;
    for(int i=0; ; i++) {
      int listGroupAndElement = tagList[i].getCombinedGroupAndElement();
      if(listGroupAndElement == groupNelement) {
        return &(tagList[i]);
      }
      if(listGroupAndElement == tagListLastGroupAndElement) break;
    }
  }
  char strptr[20 + 11 + 10];
  sprintf(strptr, "%s 0x%08x", "oh, oh, unknown tag", groupNelement);
